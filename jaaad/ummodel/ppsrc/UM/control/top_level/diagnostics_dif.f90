      Else If (.not. L_subfilter_vert) then
        sf(196,sect)=.false.
        sf(197,sect)=.false.  
      End if

      item = 190           ! momentum viscosity coeff
      If(sf(item,sect).AND.Errorstatus == 0) THEN

        Do k = 1, model_levels
          Do j = 1, rows
            Do i = 1, row_length
              work_visc(i,j,k) = visc_m(i,j,k)
            End Do
          End Do
        End Do

