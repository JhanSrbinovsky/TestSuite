        Call copydiag_3d (stashwork(si(item,sect,im_index)),            &
     &        work_visc,                                                &
     &        row_length,rows,model_levels,0,0,0,0,                     &
     &        at_extremity,                                             &
     &        stlist(1,stindex(1,item,sect,im_index)),len_stlist,       &
     &        stash_levels,num_stash_levels+1,                          &
     &        atmos_im,sect,item,                                       &
     &        Errorstatus,cmessage)

      End If

