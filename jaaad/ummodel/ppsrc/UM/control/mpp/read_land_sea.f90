         GMASKU(ROW_LENGTH,J)=GMASKT(ROW_LENGTH,J).OR.                  &
     &                        GMASKT(1,J).OR.                           &
     &                        GMASKT(ROW_LENGTH,J+1).OR.                &
     &                        GMASKT(1,J+1)
