    IF (ierror .lt. 0) THEN
      WRITE(6,*) "return code from prism_get_proto(data_64)",ierror
      WRITE(6,*) "field: ", var_id(var_ind),prism_nsec
    END IF

    ierr64 = ierror
    RETURN

  END SUBROUTINE OASIS3_get32

