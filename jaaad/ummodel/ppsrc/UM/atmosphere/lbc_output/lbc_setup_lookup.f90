!
! Declarations:

! Global parameters:
      INTEGER, PARAMETER :: model_id = 1111

!- End of COMDECK declaration

! Subroutine arguments

      Integer  :: len1_lbc_lookup
      Integer  :: n_lbc_vars
      Integer  :: lbc_lookup(len1_lbc_Lookup, n_lbc_vars)
      Integer  :: jintf
